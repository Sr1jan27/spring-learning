package com.srijan.learn_spring_framework.game;

public class MarioGame {
	
	public void up() {
		System.out.println("Jump");
	}
	
	public void down() {
		System.out.println("Go into the hole");
	}
	
	public void left() {
		System.out.println("go Forward");
	}
	
	public void right() {
		System.out.println("go Backward");
	}

}
